# config valid only for current version of Capistrano
lock '3.6.1'

set :application, 'shop_tsukamoto'
# set :repo_url, 'git@github.com:yukitsugu-tsukamoto/shop-tsukamoto-api.git'
set :repo_url, 'https://github.com/yukitsugu-tsukamoto/shop-tsukamoto-api.git'

# Default branch is :master
ask :branch, `git rev-parse --abbrev-ref HEAD`.chomp

# Default deploy_to directory is /var/www/my_app_name
set :deploy_to, '/var/www/shop_tsukamoto'

namespace :deploy do
  desc "Make sure local git is in sync with remote."
  task :confirm do
    on roles(:app) do
      puts "This stage is '#{fetch(:stage)}'. Deploying branch is '#{fetch(:branch)}'."
      puts 'Are you sure? [y/n]'
      ask :answer, 'n'
      if fetch(:answer) != 'y'
        puts 'deploy stopped'
        exit
      end
    end
  end

  desc 'Initial Deploy'
  task :initial do
    on roles(:app) do
      invoke 'deploy'
    end
  end

  before :starting, :confirm
end

